#!/usr/bin/env node
// ============================================================
// scripts/generate-solar-terms.js
// ============================================================
// Regenerates the solar terms lookup table in src/lib/bazi/solarTerms.js
// Run this once, or whenever you need to extend the year range.
//
// Usage:
//   node scripts/generate-solar-terms.js
//
// Requires: npm install --save-dev astronomia
// ============================================================

import { solstice, julian, planetposition } from 'astronomia';
import vsop87Bearth from 'astronomia/data/vsop87Bearth';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const earth = new planetposition.Planet(vsop87Bearth.default ?? vsop87Bearth);
const DEG = Math.PI / 180;

const BAZI_TERMS = [
  { name: '立春', lon: 315, yearOffset: -1 },
  { name: '惊蛰', lon: 345, yearOffset: -1 },
  { name: '清明', lon: 15,  yearOffset: 0  },
  { name: '立夏', lon: 45,  yearOffset: 0  },
  { name: '芒种', lon: 75,  yearOffset: 0  },
  { name: '小暑', lon: 105, yearOffset: 0  },
  { name: '立秋', lon: 135, yearOffset: 0  },
  { name: '白露', lon: 165, yearOffset: 0  },
  { name: '寒露', lon: 195, yearOffset: 0  },
  { name: '立冬', lon: 225, yearOffset: 0  },
  { name: '大雪', lon: 255, yearOffset: 0  },
  { name: '小寒', lon: 285, yearOffset: 0  },
];

function getTermCST(baziYear, term) {
  const jde = solstice.longitude(baziYear + term.yearOffset, earth, term.lon * DEG);
  const utc = julian.JDEToDate(jde);
  return new Date(utc.getTime() + 8 * 60 * 60 * 1000);
}

function fmt(d) {
  return d.toISOString().replace('T', ' ').substring(0, 16);
}

// ── Config ──────────────────────────────────────────────────
const START_YEAR = 1975;
const END_YEAR = 2030;
const OUTPUT_PATH = path.join(__dirname, '../src/lib/bazi/solarTerms.js');

// ── Generate ────────────────────────────────────────────────
console.log(`Generating solar terms ${START_YEAR}–${END_YEAR}...`);

const lines = [];
for (let y = START_YEAR; y <= END_YEAR; y++) {
  const times = BAZI_TERMS.map(t => `"${fmt(getTermCST(y, t))}"`);
  lines.push(`  ${y}: [${times.join(', ')}],`);
}

const output = `// ============================================================
// BaZi Solar Terms Lookup Table — ${START_YEAR} to ${END_YEAR}
// ============================================================
// AUTO-GENERATED by scripts/generate-solar-terms.js
// Source: astronomia npm package (Jean Meeus Astronomical Algorithms)
// Times are in CST (UTC+8).
//
// IMPORTANT for Indonesian users (WIB = UTC+7):
// Convert birth datetime to CST (+1 hour) before comparing.
// Example: birth at 23:30 WIB on Jan 5 = 00:30 CST on Jan 6
//
// Format per year: 12 solar terms that start each BaZi month
// [立春M1, 惊蛰M2, 清明M3, 立夏M4, 芒种M5, 小暑M6,
//  立秋M7, 白露M8, 寒露M9, 立冬M10, 大雪M11, 小寒M12]
// Note: 小寒(M12) datetime is in January of year+1
// ============================================================

export const SOLAR_TERMS_CST = {
${lines.join('\n')}
};

// Month branch lookup (index 0–11 → BaZi month 1–12)
export const MONTH_BRANCHES = ['寅','卯','辰','巳','午','未','申','酉','戌','亥','子','丑'];

/**
 * Given a birth Date object, returns the BaZi month (1–12) and branch.
 * Handles WIB-to-CST conversion internally.
 *
 * @param {Date} birthDate - JS Date object
 * @returns {{ baziMonth: number, branch: string, monthIndex: number }}
 */
export function getBaziMonth(birthDate) {
  const utcMs = birthDate.getTime();
  const cstDateObj = new Date(utcMs + 8 * 60 * 60 * 1000);
  const year = cstDateObj.getUTCFullYear();

  const currTerms = SOLAR_TERMS_CST[year];
  const prevTerms = SOLAR_TERMS_CST[year - 1];

  if (!currTerms && !prevTerms) {
    throw new Error(\`No solar term data for year \${year}. Supported: ${START_YEAR}–${END_YEAR}.\`);
  }

  let monthIndex = -1;
  let matchedTerms = null;

  if (currTerms) {
    for (let i = 0; i < 12; i++) {
      const termMs = new Date(currTerms[i].replace(' ', 'T') + ':00+08:00').getTime();
      if (utcMs >= termMs) {
        monthIndex = i;
        matchedTerms = currTerms;
      }
    }
  }

  // Date is before this year's 立春 — fall back to previous BaZi year's terms.
  // Covers early-January dates that are still in prev-year's 大雪 (M11) or 小寒 (M12).
  if (monthIndex === -1 && prevTerms) {
    for (let i = 0; i < 12; i++) {
      const termMs = new Date(prevTerms[i].replace(' ', 'T') + ':00+08:00').getTime();
      if (utcMs >= termMs) {
        monthIndex = i;
        matchedTerms = prevTerms;
      }
    }
  }

  return {
    monthIndex,
    baziMonth: monthIndex + 1,
    branch: MONTH_BRANCHES[monthIndex] ?? '?',
    termDatetime: matchedTerms?.[monthIndex] ?? null,
  };
}
`;

fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
console.log(`✓ Written to ${OUTPUT_PATH}`);
console.log(`  ${END_YEAR - START_YEAR + 1} years × 12 terms = ${(END_YEAR - START_YEAR + 1) * 12} entries`);
